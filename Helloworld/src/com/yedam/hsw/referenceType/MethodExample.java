package com.yedam.hsw.referenceType;

public class MethodExample {
	public static void main(String[] args) {

		// 메소드(method)
		int num1, num2;
		int result;

		num1 = 10;
		num2 = 20;

		result = (num1 + num2) * 3;
		System.out.println("결과값은 :" + result);
		
	}
	
	public static void main(String[] args) {
		int n1, n2, rslt;
		rslt = (n1 + n2)*3;
		System.out.println("결과값은:"+rslt);
		
	}
}
