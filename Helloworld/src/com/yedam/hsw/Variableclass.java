package com.yedam.hsw;

public class Variableclass {
	public static void main(String[] args) {

		int roll, pum, sum, num1, num2, num3;
		double pax;
		num1 = 100;
		num2 = 90;
		num3 = 80;
		sum = num1 + num2 + num3;
		pum = num1 - num3;
		roll = num2 * num3;
		pax = num1 / num3;

		System.out.println("sum의 값은: " + sum);
		System.out.println("pum의 값은:" + pum);
		System.out.println("roll의 값은:" + roll);
		System.out.println("pax의 값은:" + pax);
	}
}


